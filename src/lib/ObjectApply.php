class ObjectApply {
	
	private $_value;
	private $_function;
	private static $instance;
	private static $hash = array ();
	public function __construct($value = null) {
		$this->_value = $value;
		// echo spl_object_hash($this);
	}
	protected static function singleton($value = null, $function) {
		$class = __CLASS__;
		$kval = sha1 ( $value . $function );
		
		// New one
		if (! isset ( self::$hash [$kval] )) {
			self::$instance = new $class ( $value );
			self::$hash [$kval] = base64_encode ( serialize ( self::$instance ) );
			
			// Singleton or clone...
		} else {
			self::$instance = unserialize ( base64_decode ( self::$hash [$kval] ) );
		}
		
		return self::$instance;
	}
	public function value() {
		return $this->_value;
	}
	
	/**
	 * Check if provided string is a SHA1 hash
	 */
	public function is_sha1() {
		return ( bool ) preg_match ( '/^[0-9a-f]{40}$/i', $this->_value );
	}
	
	/**
	 * Apply PHP functions to object
	 */
	public function __call($name, $arguments = array()) {
		
		// print_r(get_defined_functions());
		$i = 0;
		$args = array ();
		
		if (function_exists ( $name )) {
			$reflect = new ReflectionFunction ( $name );
			// echo "<pre>" . $reflect . "</pre>";
			// echo $reflect->getName().' '.$reflect->getNumberOfParameters();
			foreach ( $reflect->getParameters () as $key => $param ) {
				// invokes ReflectionParameter::__toString
				if (preg_match ( '/\$?(haystack|var|str)/', $param )) {
					$args [] = $this->_value;
				} elseif (isset ( $arguments [$i] )) {
					$args [] = $arguments [$i];
					++ $i;
				}
			}
			
			$this->_function = $name;
			
			// How about chaining?
			// Can't chain with $this & _value
			// the original value will mutate
			$chain = self::singleton ( $reflect->invokeArgs ( $args ), $name );
			return $chain;
		} else {
			echo "function doesn't exists";
		}
	}

